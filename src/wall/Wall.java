package wall;

public class Wall {
    private int positionX;
    private int positionY;

    public Wall(int positionx, int positiony){
        this.positionX = positionx;
        this.positionY = positiony;
    }

    public int getPositionX() {
        return positionX;
    }

    public int getPositionY() {
        return positionY;
    }
}
